// ==================== HELPER FUNCTIONS ====================

export const filterByCategory = (category: AffiliateCategory): AffiliateProduct[] => {
  return DEFAULT_AFFILIATE_PRODUCTS.filter((p) => p.category === category);
};

export const filterByBudget = (budget: string): AffiliateProduct[] => {
  return DEFAULT_AFFILIATE_PRODUCTS.filter((p) => p.budget?.includes(budget));
};

export const filterByOccasion = (occasion: string): AffiliateProduct[] => {
  return DEFAULT_AFFILIATE_PRODUCTS.filter((p) => p.occasion?.includes(occasion));
};

export const getPopularProducts = (limit: number = 8): AffiliateProduct[] => {
  return DEFAULT_AFFILIATE_PRODUCTS
    .filter((p) => p.isPopular)
    .sort((a, b) => b.reviewCount - a.reviewCount)
    .slice(0, limit);
};

export const getFeaturedProducts = (limit: number = 6): AffiliateProduct[] => {
  return DEFAULT_AFFILIATE_PRODUCTS
    .filter((p) => p.isFeatured)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
};

export const searchProducts = (query: string): AffiliateProduct[] => {
  const lower = query.toLowerCase();
  return DEFAULT_AFFILIATE_PRODUCTS.filter(
    (p) =>
      p.name.toLowerCase().includes(lower) ||
      p.description.toLowerCase().includes(lower) ||
      p.tags?.some((t) => t.toLowerCase().includes(lower))
  );
};

export const getProductsByCategoryAndBudget = (
  category: AffiliateCategory,
  budget: string
): AffiliateProduct[] => {
  return DEFAULT_AFFILIATE_PRODUCTS.filter(
    (p) => p.category === category && p.budget?.includes(budget)
  );
};

export const formatPrice = (price: number): string => {
  return Math.round(price).toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".") + "₫";
};

// Get related products for an article based on tags matching occasions/tags
export const getRelatedProductsForArticle = (articleTags?: string[]): AffiliateProduct[] => {
  if (!articleTags || articleTags.length === 0) {
    return getPopularProducts(4);
  }
  const tagged = DEFAULT_AFFILIATE_PRODUCTS.filter(
    (p) =>
      p.occasion?.some((o) => articleTags.includes(o)) ||
      p.tags?.some((t) => articleTags.includes(t))
  );
  if (tagged.length >= 3) return tagged.slice(0, 4);
  return getPopularProducts(4);
};
