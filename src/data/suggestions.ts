// Helper functions để filter suggestions
export const filterSuggestions = (
  answers: Record<string, any>,
  maxResults: number = 10
): Suggestion[] => {
  let scored = suggestions.map(suggestion => ({
    suggestion,
    score: calculateScore(suggestion, answers),
  }));

  // Sort by score descending
  scored.sort((a, b) => b.score - a.score);

  // Return top results
  return scored.slice(0, maxResults).map(item => item.suggestion);
};

const calculateScore = (suggestion: Suggestion, answers: Record<string, any>): number => {
  let score = 0;

  // Gender match (10 points)
  if (!suggestion.gender || suggestion.gender === 'Both' || suggestion.gender === answers.gender) {
    score += 10;
  }

  // Budget match (10 points)
  if (suggestion.budget.includes(answers.budget)) {
    score += 10;
  }

  // Occasion match (10 points)
  if (suggestion.occasion.includes(answers.occasion)) {
    score += 10;
  }

  // Personality match (20 points - can match multiple)
  if (answers.personality && Array.isArray(answers.personality)) {
    const matchCount = suggestion.personality.filter(p =>
      answers.personality.includes(p)
    ).length;
    score += matchCount * 7; // Max 21 points if 3 match
  }

  // Hobbies match (20 points - can match multiple)
  if (answers.hobbies && Array.isArray(answers.hobbies)) {
    const matchCount = suggestion.hobbies.filter(h =>
      answers.hobbies.includes(h)
    ).length;
    score += matchCount * 5; // Max 25 points if 5 match
  }

  // Love language match (15 points)
  if (suggestion.loveLanguage && answers.love_language) {
    if (suggestion.loveLanguage.includes(answers.love_language)) {
      score += 15;
    }
  }

  // Relationship stage match (15 points)
  if (suggestion.relationshipStage && answers.relationship_duration) {
    if (suggestion.relationshipStage.includes(answers.relationship_duration)) {
      score += 15;
    }
  }

  return score;
};

// Get suggestions by type
export const getSuggestionsByType = (
  type: 'gift' | 'experience' | 'activity' | 'romantic_plan'
): Suggestion[] => {
  return suggestions.filter(s => s.type === type);
};
